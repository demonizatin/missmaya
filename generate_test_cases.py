# ---------- styling helpers ----------
HDR_FILL = PatternFill("solid", fgColor="C2643B")
HDR_FONT = Font(bold=True, color="FFFFFF", size=11)
P1_FILL = PatternFill("solid", fgColor="FBEAEA")  # high
P2_FILL = PatternFill("solid", fgColor="FDF6ED")  # med
P3_FILL = PatternFill("solid", fgColor="F4F1EA")  # low
THIN = Side(border_style="thin", color="D0D0D0")
BORDER = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)
WRAP = Alignment(wrap_text=True, vertical="top")

COLS = [
    ("ID", 10),
    ("Title", 36),
    ("Preconditions", 30),
    ("Steps", 60),
    ("Expected result", 50),
    ("Priority", 10),
    ("Status", 12),
    ("Notes", 24),
]


def add_sheet(wb, name, intro, cases):
    ws = wb.create_sheet(name)
    ws.row_dimensions[1].height = 32
    ws.cell(1, 1, intro).font = Font(italic=True, size=10, color="555555")
    ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=len(COLS))
    ws.cell(1, 1).alignment = Alignment(wrap_text=True, vertical="center")

    for c, (label, width) in enumerate(COLS, 1):
        cell = ws.cell(2, c, label)
        cell.fill = HDR_FILL
        cell.font = HDR_FONT
        cell.alignment = Alignment(vertical="center", horizontal="left")
        ws.column_dimensions[get_column_letter(c)].width = width
    ws.row_dimensions[2].height = 22

    for r, case in enumerate(cases, 3):
        for c, key in enumerate(["id", "title", "pre", "steps", "expected", "pri", "status", "notes"], 1):
            cell = ws.cell(r, c, case.get(key, ""))
            cell.alignment = WRAP
            cell.border = BORDER
            cell.font = Font(size=10)
        pri = case.get("pri", "P2")
        fill = P1_FILL if pri == "P1" else P2_FILL if pri == "P2" else P3_FILL
        ws.cell(r, 6).fill = fill
        # set row height proportional to longest field
        longest = max(len(case.get(k, "")) for k in ("steps", "expected"))
        ws.row_dimensions[r].height = min(180, max(40, longest // 2))

    ws.freeze_panes = "A3"
    ws.sheet_view.showGridLines = False


def tc(id_, title, pre, steps, expected, pri="P2", notes=""):
    return {"id": id_, "title": title, "pre": pre, "steps": steps,
            "expected": expected, "pri": pri, "status": "", "notes": notes}


